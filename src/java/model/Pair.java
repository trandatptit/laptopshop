package model;

/**
 *
 * @author PC
 */
public class Pair {
    private double first, second;

    public Pair() {
    }

    public Pair(double first, double second) {
        this.first = first;
        this.second = second;
    }

    public double getFirst() {
        return first;
    }

    public double getSecond() {
        return second;
    }

    public void setFirst(double first) {
        this.first = first;
    }

    public void setSecond(double second) {
        this.second = second;
    }
    
    
}
