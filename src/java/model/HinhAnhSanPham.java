package model;

/**
 *
 * @author PC
 */
public class HinhAnhSanPham {
    private int id, pid;
    private String url;

    public HinhAnhSanPham() {
    }

    public HinhAnhSanPham(int id, int pid, String url) {
        this.id = id;
        this.pid = pid;
        this.url = url;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
    
    
}
